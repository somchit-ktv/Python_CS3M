# ການຈັບຄູ່ສີນຄ້າ ແລະ ລາຄາ

# ສ້າງ list ຂື້ນມາເພື່ອເກັບຂໍ້ມູນຂອງສີນຄ້າແລະລາຄາ
product=["pepsi","beer","water"]
price=[5000,9000,3000]

# ກຳນົດ x ແລະ y ເປັນໂຕນັບຂອງ list ສີນຄ້າ ແລະ ລາຄາ
# ໃຊ້ຟັງຊັນ zip ເພື່ອຈັບຄູ່ສີນຄ້າ ແລະ ລາຄາ
for x,y in zip(product,price):          
    print("ສີນຄ້າ = ",x , "ລາຄາ = ",y)