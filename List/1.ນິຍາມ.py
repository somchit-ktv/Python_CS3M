# primitive variable / ແມ່ນປະເພດຂໍ້ມູນທີ່ອະນຸຍາດໃຫ້ໂຕແປໂຕລະໂຕມີພຽງຄ່າດຽວເທົ່ານັ້ນ ເຊັ່ນ Integer, String, Boolean ເປັນຕົ້ນ
a = 1
a1 = 2
a2 = 3

# non-primitive variable : list  / ແມ່ນປະເພດຂໍ້ມູນທີ່ສາມາດມີຫຼາຍຄ່າໃນໂຕແປດຽວ ເຊັ່ນ Array, List, Set, Tuple ເປັນຕົ້ນ
number = [] #list empty
number = [1,2,3,4,5]
name = ["John", "Chiele", "Mike", "Kole", "Doley"]
etc = [1,"Max",3.25,True,-20]

# constructure ແມ່ນການສ້າງຮູບແບບຂອງຂໍ້ມູນໂດຍລະບຸບປະເທດໃສ່ກ່ອນຈະຖີິງສະມາຊິກຂອງຂໍ້ມູນ
name = list(["John", "Chiele", "Mike", "Kole", "Doley"])
