# ການຄົ້ນຫາ ແລະ ນັບຈຳນວນຂອງສະມາຊິກທີ່ມີຄ່າຊ້ຳກັນໃນ tuple ສາມາດເຮັດໄດ້ໂດຍໃຊ້ .count

name = ("John","Chiele","Mike","Kole","Doley","Jame","John", "William","Smith","Jerry","John")

# ສ້າງໂຕແປຂຶ້ນມາເພື່ອໃຊ້ໃນການເກັບຄ່າ Integer ຂອງຈຳນວນສະມາຊິກທີ່ຊ້ຳກັນ
x = name.count("John")
print("ຈຳນວນຂອງສະມາຊິກຄືກັນມີ : ", x, "ໂຕ")

# ໃນການຄົ້ນຫາຕຳແໜ່ງຂອງສະມາຊິກວ່າຢູ່ໃນຕຳແໜ່ງທີ່ເທົ່າໃດຂອງ tuple ສາມາດໃຊ້ .index ໃນການຄົ້ນຫາ
x = name.index("Doley")
print("ຕຳແໜ໋ງຂອງສະມາຊິກແມ່ນ : ", x)