# ຕົວຢ່າງ 7: ໂອນຄ່າ tuple ໄປໃສ່ໂຕແປ

name = ("John", "Chiele", "Mike", "Kole", "Doley")
# ກຳນົດໂຕແປທີ່ຈະມາຮັບຄ່າສະມາຊິກແຕ່ລະໂຕຈາກ tuple
a, b, c, d, e = name
print(a) # ຜົນລັບທີ່ໄດ້ John
print(b) # ຜົນລັບທີ່ໄດ້ Chiele
print(c) # ຜົນລັບທີ່ໄດ້ Mike
print(d) # ຜົນລັບທີ່ໄດ້ Kole
print(e) # ຜົນລັບທີ່ໄດ້ Doley